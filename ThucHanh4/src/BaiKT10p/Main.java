    private Thread t;
    private class Thread1 extends Thread{
        JCheckBox[] checkBox = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10};
        int time = 10;
        @Override
        public void run() {
            for (JCheckBox j : checkBox) {
                j.setEnabled(true);
                j.setSelected(false);}
            while(time > 0){
                try {
                    sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);}
                time--;
                jLabel1.setText("Thoi gian " + time + " p");
                if(time == 0){
                    int diem = 0;
                    for (JCheckBox jCheckBox : checkBox) {
                        if(jCheckBox.isSelected())
                            diem++;}
                    JOptionPane.showMessageDialog(rootPane, "tg da het , diem ban la : " + diem);
                }
            }
        }
        
    }
