    private class Thread1 extends Thread{ // nhaasap nháy 3 màu jlabel
        int i = 0;
        
        @Override
        public void run() {
            while(true){
                if(i == 0)
                    jLabel1.setForeground(Color.red);
                else if(i == 1)
                    jLabel1.setForeground(Color.green);
                else if(i == 2)
                    jLabel1.setForeground(Color.pink);
                i++;
                i = i % 3;
                try {
                    sleep(500);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }
    
    private class Thread2 extends Thread{

        @Override
        public void run() {
            while(true){
                String s = jLabel2.getText();
                String[] t = s.split("");
                String text = "";
                text += t[t.length-1];
                for (int i = 0; i < t.length - 1; i++) {
                    text += t[i];
                    
                }
                jLabel2.setText(text);
                try {
                    sleep(500);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
            
        }
        
    }
