    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String s = "Cal by "+jList1.getSelectedValue();
        String[] t = jTextField1.getText().split(" ");
        List<Integer> arr = new ArrayList<>();//cho vào arraylist để dùng collection tìm max min cho nhanh
        for (String x : t) {
            arr.add(Integer.parseInt(x));
        }
        if(jRadioButton1.isSelected()){
            int sum = 0;
        for (Integer i : arr) {
            sum += i;
        }
        s += "\n" + "SUM = "+sum;
        } 
        if(jRadioButton2.isSelected())
            s += "\n" + "max = " + Collections.max(arr);
        if(jRadioButton3.isSelected())
            s += "\n" + "min = " + Collections.min(arr);
        
        jTextArea1.setText(s);
    }//GEN-LAST:event_jButton1ActionPerformed

