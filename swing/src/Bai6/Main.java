    private String getEmail() {
        String[] t = jTextField2.getText().split(" ");
        String email = t[t.length - 1]; //lấy chữ cái đầu của text nhâp vào; mảng gồm n phần tử thì chỉ số bắt đầu từ 0 đến (n-1) 
        for (int i = 0; i < t.length - 1; i++) {
            email += t[i].charAt(0);
            
        }
        email +="@ptit.edu.vl";
        return email;
    }
