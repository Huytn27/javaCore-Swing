    public String xuLy(){
        String s = "";
        s += "teacher " + jTextField1.getText();
        String[] t = m.getTextArial().split(" ");
        int dem = 0 ;
        for (String x : t) {
            if (x.equalsIgnoreCase("toi"))
                dem++;
        }
        if(dem > 4)
            s += "qua nhieu toi";
        else if(dem < 2)
            s += "qua it toi";
        else if(dem <= 4 && dem >= 2 )
            s +="OK";
        if(jCheckBox1.isSelected())
            s += "\n contact by " + jTextField2.getText();
        return s;
    }

