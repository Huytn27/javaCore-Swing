    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int[][] d;
        try {
            BufferedReader bf = new BufferedReader(new FileReader("src/com/minhtanit/IOFile/vidu.txt"));
            String line = bf.readLine();
            jTextArea1.setText(line);
            String[] t = line.split("\\s+");
            setN(Integer.parseInt(t[0]));
            setM(Integer.parseInt(t[1]));
            d = new int[getN()][getM()];
            for (int i = 0; i < d.length; i++) {
                String s = bf.readLine();
                jTextArea1.append("\n" + s);
                t = s.split("\\s+");
                for (int j = 0; j < d[0].length; j++) {// cột
                    d[i][j] = Integer.parseInt(t[j]);// chuyển các phần tử trong mảng String t thành int rồi lưu vào mảng d
                }
            }
            a = d;
            d = new int[getN()][getM()];
            for (int i = 0; i < d.length; i++) {
                String s = bf.readLine();
                jTextArea1.append("\n" + s);
                t = s.split("\\s+");
                for (int j = 0; j < d[0].length; j++) {
                    d[i][j] = Integer.parseInt(t[j]);
                }
            }
            b = d;
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Main3.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Main3.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PrintWriter pw = new PrintWriter("src/com/minhtanit/IOFile/kq.txt");
            for (int i = 0; i < getA().length; i++) {
                for (int j = 0; j < getA()[0].length; j++) {
                    pw.write(String.valueOf(a[i][j] + b[i][j]) + "  ");
                }
                pw.print("\n");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Main3.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

