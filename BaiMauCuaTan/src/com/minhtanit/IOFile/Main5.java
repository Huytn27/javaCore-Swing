    private List<Student> list = new ArrayList<>();
    private void loadData() {
        model.setRowCount(0);
        list = IOFile2.readFile("src/com/minhtanit/IOFile/student.txt");
        for (Student student : list) {
            model.addRow(student.toObjects());
        }
    }
    
    private void writeData() {
        IOFile2.writeFile(list, "src/com/minhtanit/IOFile/student.txt");
    }
