    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jTextArea1.setText("");
        String[] t = jTextField2.getText().split("\\s+");
        if(jRadioButton2.isSelected()) {
            for (String string : t) {
                if(isNguyenTo(Integer.parseInt(string)))
                    jTextArea1.append(string + " ");
            }
        } else if(jRadioButton1.isSelected()) {
            for (String string : t) {
                if(Integer.parseInt(string) % 2 == 1) 
                    jTextArea1.append(string);
            }
        } else if(jRadioButton3.isSelected()) {
            for (String string : t) {
                if(Integer.parseInt(string) % 2 == 0) 
                    jTextArea1.append(string);
            }
        }
        if(!jCheckBox1.isSelected()) 
            jTextArea1.append("\nHo ten: " + jList1.getSelectedValue());
        else {
            jTextArea1.append("\n" + jList1.getSelectedValuesList().toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

