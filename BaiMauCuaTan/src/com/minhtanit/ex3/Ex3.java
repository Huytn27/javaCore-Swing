    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] t = jTextArea1.getText().split("\\s");
        int dem = 0;
        for(String x : t) {
            if(x.equalsIgnoreCase("toi"))
                dem++;
        }
        String mess;
        if(dem < 2)
            mess = "too few \"toi\" world";
        else if( dem > 4) 
            mess = "too more \"toi\" world";
        else
            mess = "OK";
        if(jCheckBox1.isSelected()) {
            jTextArea1.setText("Teacher: " + jTextField1.getText() +  "\n" + mess + "\n" + "Contact: " + jTextField2.getText());
        } else {
            jTextArea1.setText("Teacher: " + jTextField1.getText() +  "\n" + mess );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

