    public List<Integer> getNumbers() {
        String[] t = jTextField1.getText().split("\\s+");
        List<Integer> list = new ArrayList<>();
        for (int i = 0; i < t.length; i++) {
            list.add(Integer.parseInt(t[i])); 
        }
        return list;
    }
    
