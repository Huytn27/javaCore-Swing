    private class Thread1 extends Thread{

        @Override
        public void run() {
            while(true){
                String s = jTextArea1.getText();
                jTextArea1.setText("");
                String[] t = s.split("");
                for (int i = 1; i < t.length; i++) {
                    jTextArea1.append(t[i]);                
                }
                jTextArea1.append(t[0]);
                try {
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                    Logger.getLogger(ex1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
        
    }
    private class Thread2 extends Thread{

        @Override
        public void run() {
            while(true){
                String s = jLabel1.getText();
                String text = "";
                String[] t = s.split("");
                for (int i = 1; i < t.length; i++) {
                    text += t[i];                
                }
                text += t[0];
                jLabel1.setText(text);
                try {
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                    Logger.getLogger(ex1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
        
    }
