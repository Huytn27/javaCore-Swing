    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        List<MatHang> a=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            a.add(new MatHang(tm.getValueAt(i,0).toString(),
                    tm.getValueAt(i,1).toString(),
                    tm.getValueAt(i,2).toString(),
                    Double.parseDouble(tm.getValueAt(i,3).toString())));
            
        }
        IOFile.vietFile(a, filename);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        List<MatHang> a=new ArrayList<>();
        IOFile.docFile(a, filename);
        for (int i = 0; i < a.size(); i++) {
            tm.addRow(a.get(i).toObject());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

