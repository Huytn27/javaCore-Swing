    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String s = "Teacher : " + jTextField1.getText() + "\n";
        String[] t = m.getText().split("\\s+");
        int dem = 0;
        for (String x : t){
            if(x.equalsIgnoreCase("toi"))
                dem++;
        }
        if(dem > 4)
            s += "qua nhieu toi";
        else if( dem < 2)
            s += "qua it toi ";
        else if(dem >= 2 && dem <= 4)
            s += " OK ";
        if(jCheckBox1.isSelected())
            s += "\ncontact by" + jTextField2.getText();
        m.setText(s);
        
    }//GEN-LAST:event_jButton1ActionPerformed

