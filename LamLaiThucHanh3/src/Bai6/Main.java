    public String Email(){
        String s = "contact to ";
        String[] t = jTextField2.getText().split(" ");
        // viết hoa chữ cái đầu của tên nếu nhập tên ko viết hoa;
        // vidu : vu lan huong => Huongvl@gmail.com
        // replace chữ cái đầu của tên = chữ cái đầu của tên sau khi đã toUpperCase;
        String ten = t[t.length-1].replace(t[t.length-1].charAt(0), Character.toUpperCase(t[t.length-1].charAt(0)));
        s += ten;
        for (int i = 0; i < t.length-1; i++) {
            s += t[i].charAt(0);
            
        }
        return s + "@gamil";
    }
    
