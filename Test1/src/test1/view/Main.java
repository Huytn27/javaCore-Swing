    private DefaultTableModel bookTableModel;
    private DefaultTableModel readerTableModel;
    private List<Book> bookList = new ArrayList<>();
    private List<Reader> readerList = new ArrayList<>();

    private void initBookTable() {
        bookTableModel.setRowCount(0);
        for (Book book : bookList) {
            bookTableModel.addRow(book.toObjects());
        }
        initCodeBook();
    }

    private void initCodeBook() {
        List<Integer> codeList = new ArrayList<>();
        for (Book book : bookList) {
            codeList.add(book.getCode());
        }
        for (int i = 10000; i < 99999; i++) {
            if (!codeList.contains(i)) {
                jTextField1.setText(String.valueOf(i));
                return;
            }
        }
    }
    
    private void initReaderTable() {
        readerTableModel.setRowCount(0);
        for (Reader reader : readerList) {
            readerTableModel.addRow(reader.toObjects());
        }
        initCodeReader();
    }

    private void initCodeReader() {
        List<Integer> codeList = new ArrayList<>();
        for (Reader reader : readerList) {
            codeList.add(reader.getCode());
        }
        for (int i = 10000; i < 99999; i++) {
            if (!codeList.contains(i)) {
                jTextField6.setText(String.valueOf(i));
                return;
            }
        }
    }
