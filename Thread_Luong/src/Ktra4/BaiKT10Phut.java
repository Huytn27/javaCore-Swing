    private class Thread1 extends Thread{
        JCheckBox[] arrCheckBox = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10};
        int time = 10;
        @Override
        public void run() {
            for (JCheckBox j : arrCheckBox) {
                j.setEnabled(true);
                j.setSelected(false); 
            }
            
            while(time > 0){
                
                try {
                    sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(BaiKT10Phut.class.getName()).log(Level.SEVERE, null, ex);
                }
                time--;
                jLabel1.setText("thoi gian : " + time + "p");
                if(time == 0){
                    int diem = 0;
                    for (JCheckBox j : arrCheckBox) {
                        if(j.isSelected())
                            diem++;
                    }
                    JOptionPane.showMessageDialog(rootPane, "Thoi gian da het \nDiem ban la : " + diem);
                }
                    
            }
        }
        
    }
