    private class Thread1 extends Thread{
        JButton[] listB = {jButton1, jButton2, jButton3, jButton4, jButton5};
        int index = 0; //chỉ số button dc xét màu đầu tiên
        @Override
        public void run() {
            while(true){
                List<Color> colors = new ArrayList<>();
                if(jCheckBox1.isSelected())
                    colors.add(Color.red);
                 if(jCheckBox2.isSelected())
                    colors.add(Color.green);
                if(jCheckBox3.isSelected())
                    colors.add(Color.yellow);
                if(jCheckBox4.isSelected())
                    colors.add(Color.black);
                if(colors.size() > 0){
                    if(jRadioButton1.isSelected()){
                    for (int i = 0; i < listB.length; i++) {
                        listB[(i+index) % listB.length].setBackground(colors.get(i % colors.size()));
                        try {
                            sleep(300);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(NhapNhayNhieuButton.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
               
                else if(jRadioButton2.isSelected()){
                    for (int i = 0; i < listB.length; i++) {
                        listB[(i+index) % listB.length].setForeground(colors.get(i % colors.size()));
                    }
                }
                }
                else{
                    try {
                        sleep(2500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(NhapNhayNhieuButton.class.getName()).log(Level.SEVERE, null, ex);
                    }
                
                }
                index = (index + 1) % listB.length; // tăng index lên 1 đơn vị nhưng ko quá lenght của B;
                // số dư khoog vượt quá số chia;
            }
        
        }
    }
