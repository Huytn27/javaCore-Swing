    private void nhapBangSach(){
        model1.setRowCount(0);
        for (Sach sach : listSach) {
            model1.addRow(sach.toObjects());
        }
        nhapMaSach();
    }
    
    private void nhapMaSach(){
        List<Integer> listMaS = new ArrayList<>();
        for (Sach s : listSach) {
            listMaS.add(s.getMaSach());
        }
        
        for (int i = 10000; i <= 90000; i++) {
            if(!listMaS.contains(i)){
                jTextField1.setText(String.valueOf(i));
                return;
            }
        }
    }
