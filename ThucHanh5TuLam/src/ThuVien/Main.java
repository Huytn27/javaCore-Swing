    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Sach2 sach = sachList.get(jTable1.getSelectedRow());
        jTextField2.setText(sach.getTen());
        jTextField3.setText(sach.getTacGia());
        for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            if(sach.getChuyenNganh().equals(jComboBox1.getItemAt(i))) {
                jComboBox1.setSelectedIndex(i); //hiển thị giá trị trong jcomboBox ở vị trí đã chọn (vị trí i)
                break;
            }
            
        }
        jTextField4.setText(String.valueOf(sach.getNam()));
        jTextField5.setText(String.valueOf(sach.getSoLuong()));
    }//GEN-LAST:event_jTable1MouseClicked

