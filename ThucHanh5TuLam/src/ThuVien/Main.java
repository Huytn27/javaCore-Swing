    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        List<BanDoc2> ban = new ArrayList<>();
        for (BanDoc2 x : banDocList) {
            if(x.getTen().equalsIgnoreCase(jTextField10.getText()))
                ban.add(x);
        }
        
        if(ban.isEmpty())//nếu list rỗng nghĩa là ko dc thêm vào cái gì
            JOptionPane.showMessageDialog(this, "ko tim dc");
        else {
            banDocTableModel.setRowCount(0);
            for (BanDoc2 b : ban) {
                banDocTableModel.addRow(b.toObjects());
            }
        }
            
    }//GEN-LAST:event_jButton10ActionPerformed

