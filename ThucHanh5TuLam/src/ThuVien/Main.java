    private List<Sach2> sachList = new ArrayList<>();
    private List<BanDoc2> banDocList = new ArrayList<>();
    
    private void initSachTable(){
        sachTableModel.setRowCount(0);
        for (Sach2 sach2 : sachList) {
            sachTableModel.addRow(sach2.toObjects());
        }
        initMa();
    }
    
    private void initMa(){
        List<Integer> listMa = new ArrayList<>();
        for (Sach2 sach2 : sachList) {
            listMa.add(sach2.getMa());
        }
        
        for (int i = 10000; i < 999999; i++) {
            if(!listMa.contains(i)){//nếu ko trùng với i 
                jTextField1.setText(String.valueOf(i));
                return ;
            }
            
        }
    }
    
    private void initBanDocTable(){
        banDocTableModel.setRowCount(0);
        for (BanDoc2 banDoc2 : banDocList) {
            banDocTableModel.addRow(banDoc2.toObjects());
        }
        initMaBanDoc();
    }
    
    private void initMaBanDoc(){
        List<Integer> listMaBanDoc = new ArrayList<>();
        for (BanDoc2 x : banDocList) {
            listMaBanDoc.add(x.getMa());
        }
        
        for (int i = 10000; i <= 99999; i++) {
            if(!listMaBanDoc.contains(i)){ //nếu trong list chưa chứa i
                jTextField6.setText(String.valueOf(i));
                return;
            }
        }
    }
